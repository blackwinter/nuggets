module Nuggets
  class Array
    module LimitMixin

  # call-seq:
  #   array.limit(min, max) => new_array
  #
  # Returns a new array of all distinct values in _array_ limited to +min+
  # and +max+ (cf. Numeric#limit).
  def limit(min, max)
    map { |item| item.between(min, max) }.uniq
  end

  alias_method :between, :limit

    end
  end
end
