module Nuggets
  class Integer
    module MapMixin

  # call-seq:
  #   int.map_positive => anInteger
  #
  # Creates a bijection from *Z* to *N*, i.e., it maps _int_ to a positive
  # integer.
  def map_positive
    2 * abs + (self > 0 ? 0 : 1)
  end

    end
  end
end
